    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
          if (OldBalance < 100) {
            JOptionPane.showMessageDialog(this, "Not Enough Funds");
        } else {
            try {
                // Update query with correct SQL syntax
                String Query = "UPDATE AccountTbl SET Balance = ? WHERE AccNum = ?";
                con = DriverManager.getConnection("jdbc:mysql://localhost:3307/atmdb", "root", "");
                PreparedStatement ps = con.prepareStatement(Query);
                ps.setInt(1, OldBalance - 100); // Subtract 100 from OldBalance
                ps.setInt(2, MyAccNum); // Use MyAccNum as account number

                if (ps.executeUpdate() == 1) {
                    JOptionPane.showMessageDialog(this, "₹100 Transfer Successfully");
                    new MAINMENU(MyAccNum).setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to update balance");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            } 
            }
        
    


    }//GEN-LAST:event_jButton9ActionPerformed

