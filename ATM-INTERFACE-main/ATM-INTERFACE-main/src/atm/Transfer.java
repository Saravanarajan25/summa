       private int GetBalance() {
        String Query = "SELECT Balance FROM accounttbl WHERE AccNum= '" + MyAccNum + "'";
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3307/atmdb", "root", "");
            st = con.createStatement();
            rs = st.executeQuery(Query);
            if (rs.next()) {
                return rs.getInt(1); // Corrected index to 1
            } else {
                JOptionPane.showMessageDialog(this, "Account number is incorrect.");
                return 0; // Return 0 or handle as needed
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error fetching balance: " + e.getMessage());
            return 0; // Return 0 or handle as needed
        } finally {
            try {
                if (rs != null) rs.close();
                if (st != null) st.close();
                if (con != null) con.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error closing resources: " + e.getMessage());
            }
        }
    }
