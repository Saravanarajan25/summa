  private void GetBalance() {  
    String Query = "select Balance from AccountTbl where AccNum= '"+MyAccNum+"'";
    try {
        con = DriverManager.getConnection("jdbc:mysql://localhost:3307/atmdb", "root", "");
        st = con.createStatement();
        rs = st.executeQuery(Query);
        if(rs.next()) {
            OldBalance = rs.getInt("Balance");
            System.out.println(OldBalance);
        } else {
            JOptionPane.showMessageDialog(this, "Account Number or Phone incorrect" );
        }
    } catch(Exception e) {
        JOptionPane.showMessageDialog(this, "Account Number or Phone incorrect" );
    }
}



